def argument_parser():
    description = 'Example of an OFDM receiver'
    parser = ArgumentParser(description=description)
    parser.add_argument(
        "--args", dest="args", type=str, default="",
        help="Set Arguments [default=%(default)r]")
    parser.add_argument(
        "--duration", dest="duration", type=intx, default=60,
        help="Set Duration [default=%(default)r]")
    parser.add_argument(
        "--freq", dest="freq", type=eng_float, default="3.52G",
        help="Set Center Frequency [default=%(default)r]")
    parser.add_argument(
        "--rx-gain", dest="rx_gain", type=eng_float, default="30.0",
        help="Set RX Gain [default=%(default)r]")
    parser.add_argument(
        "--samp-rate", dest="samp_rate", type=intx, default=500000,
        help="Set Sample Rate [default=%(default)r]")
    parser.add_argument(
        "--source", dest="source", type=str, default='uhd',
        help="Set Source [default=%(default)r]")
    parser.add_argument(
        "--zmq-rx-address", dest="zmq_rx_address", type=str, default='tcp://localhost:5001',
        help="Set ZMQ Rx Address [default=%(default)r]")
    return parser


def main(top_block_cls=rx_ofdm, options=None):
    if options is None:
        options = argument_parser().parse_args()
    tb = top_block_cls(args=options.args, duration=options.duration, freq=options.freq, rx_gain=options.rx_gain, samp_rate=options.samp_rate, source=options.source, zmq_rx_address=options.zmq_rx_address)

    def sig_handler(sig=None, frame=None):
        tb.stop()
        tb.wait()

        sys.exit(0)

    signal.signal(signal.SIGINT, sig_handler)
    signal.signal(signal.SIGTERM, sig_handler)

    tb.start()

    tb.wait()


if __name__ == '__main__':
    main()
